def generate_connection_string(db_type: Literal["mysql", "postgresql", "redis"] = "postgresql") -> str:
    """
    Generate a synthetic database connection string for honeypot scenarios.
    
    Args:
        db_type: Type of database connection string to generate
    
    Returns:
        str: Honeypot connection string with safe credentials
    """
    username = f"hp_demo_user_{secrets.token_hex(2)}"
    password = generate_db_password(db_type.upper())
    host = f"demo-db-{secrets.token_hex(2)}.internal"
    
    if db_type == "mysql":
        return f"mysql://{username}:{password}@{host}:3306/production_db"
    elif db_type == "postgresql":
        return f"postgresql://{username}:{password}@{host}:5432/production_db"
    elif db_type == "redis":
        return f"redis://:{password}@{host}:6379/0"
    else:
        return f"{db_type}://{username}:{password}@{host}/production_db"


# ── Passwords & Authentication ─────────────────────────────────────────────────


def generate_user_password(username: str = "user") -> str:
    """
    Generate a synthetic user password for honeypot scenarios.
    
    Args:
        username: Username the password is for (used for variety)
    
    Returns:
        str: Honeypot password (e.g., HP_DEMO_AdminPassword_7F8E)
    """
    random_hex = secrets.token_hex(2).upper()
    username_safe = username.replace("_", "").replace("-", "").title()
    return f"{HONEYPOT_PREFIX}{username_safe}Password_{random_hex}"


def generate_password_hash() -> str:
    """
    Generate a synthetic bcrypt-style password hash for honeypot scenarios.
    
    Real bcrypt hashes start with $2b$ and are 60 characters.
    This generates a realistic-looking but non-functional hash.
    
    Returns:
        str: Honeypot password hash (e.g., $HP_DEMO$12$A1B2C3D4E5F6...)
    """
    random_data = secrets.token_bytes(32)
    hash_suffix = hashlib.sha256(random_data).hexdigest()[:40]
    return f"$HP_DEMO$12${hash_suffix}"


