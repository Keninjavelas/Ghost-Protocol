# ── Credential Detection Helper ────────────────────────────────────────────────


def is_honeypot_credential(credential: str) -> bool:
    """
    Check if a credential string is a Ghost Protocol honeypot credential.
    
    Args:
        credential: The credential string to check
    
    Returns:
        bool: True if the credential is a honeypot credential
    """
    return credential.startswith(HONEYPOT_PREFIX)


def extract_credential_type(credential: str) -> str | None:
    """
    Extract the credential type from a honeypot credential string.
    
    Args:
        credential: The credential string to analyze
    
    Returns:
        str | None: The credential type (e.g., "AWS_ACCESS_KEY") or None if not a honeypot credential
    """
    if not is_honeypot_credential(credential):
        return None
    
    # Remove prefix and extract type
    without_prefix = credential[len(HONEYPOT_PREFIX):]
    
    # Split by underscore and rejoin until we hit a hex pattern
    parts = without_prefix.split("_")
    
    # Find where the random hex starts (typically after the last meaningful part)
    type_parts = []
    for part in parts:
        # If part looks like hex and is 4+ chars, it's likely the random suffix
        if len(part) >= 4 and all(c in "0123456789ABCDEF" for c in part):
            break
        type_parts.append(part)
    
    return "_".join(type_parts) if type_parts else None


