"""
ai_core/demo_credentials.py
Honeypot-safe credential generator for Ghost Protocol.

Generates synthetic credentials that:
• Look realistic to attackers during honeypot sessions
• Never match real credential formats (no GitHub secret scanning triggers)
• Cannot be used against production systems
• Are clearly marked as demo/honeypot credentials internally

All generated credentials use the HP_DEMO_ prefix to:
1. Prevent accidental use against real services
2. Enable internal detection of credential exfiltration attempts
3. Avoid triggering Git secret scanning tools
4. Maintain clear attribution as demonstration-only data

SECURITY: These credentials are intentionally non-functional and designed
only for cyber deception scenarios. They follow OWASP honeypot best practices.
"""
from __future__ import annotations

import secrets
import hashlib
from typing import Literal

# ── Honeypot Prefix Convention ─────────────────────────────────────────────────
# All credentials generated by this module use the HP_DEMO_ prefix.
# This ensures they are identifiable as honeypot artifacts and will not
# trigger GitHub secret scanning or be mistaken for real credentials.

HONEYPOT_PREFIX = "HP_DEMO_"

# ── AWS-Style Credentials ──────────────────────────────────────────────────────


def generate_aws_access_key() -> str:
    """
    Generate a synthetic AWS access key for honeypot scenarios.
    
    Real AWS keys start with AKIA and are 20 characters.
    This generates a safe alternative that resembles AWS format.
    
    Returns:
        str: Honeypot AWS access key (e.g., HP_DEMO_AWS_ACCESS_KEY_A1B2C3D4)
    """
    random_hex = secrets.token_hex(4).upper()
    return f"{HONEYPOT_PREFIX}AWS_ACCESS_KEY_{random_hex}"


def generate_aws_secret_key() -> str:
    """
    Generate a synthetic AWS secret key for honeypot scenarios.
    
    Real AWS secrets are 40 characters of base64-like data.
    This generates a safe alternative.
    
    Returns:
        str: Honeypot AWS secret key (e.g., HP_DEMO_AWS_SECRET_9F8E7D6C5B4A)
    """
    random_hex = secrets.token_hex(6).upper()
    return f"{HONEYPOT_PREFIX}AWS_SECRET_{random_hex}"


